function bm()
{
    var k = document.getElementById("bm")
    if(k.style.color == "black"){
    k.style.fontSize = "48pt"
    k.style.color = "white"
    k.style.background = "blue"
    }
    else {
        k.style.fontSize = "12pt"
        k.style.color = "black"
        k.style.background = "white"
    }
}

